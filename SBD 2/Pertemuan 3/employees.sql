CREATE TABLE employees
  (
    emp_id INTEGER PRIMARY KEY,
    emp_name VARCHAR(15),
    job_name VARCHAR(10),
    manager_id INTEGER,
    hire_date DATE,
    salary DECIMAL(10, 2),
    comission DECIMAL(7, 2),
    dep_id INTEGER,
    FOREIGN KEY (dep_id) REFERENCES department(dep_id)
  );

